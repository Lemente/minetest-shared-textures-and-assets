local function rotate_and_place(itemstack, placer, pointed_thing)
	local p0 = pointed_thing.under
	local p1 = pointed_thing.above
	local param2 = 0

	if placer then
		local placer_pos = placer:get_pos()
		if placer_pos then
			param2 = minetest.dir_to_facedir(vector.subtract(p1, placer_pos))
		end

		local finepos = minetest.pointed_thing_to_face_pos(placer, pointed_thing)
		local fpos = finepos.y % 1

		if p0.y - 1 == p1.y or (fpos > 0 and fpos < 0.5)
				or (fpos < -0.5 and fpos > -0.999999999) then
			param2 = param2 + 20
			if param2 == 21 then
				param2 = 23
			elseif param2 == 23 then
				param2 = 21
			end
		end
	end
	return minetest.item_place(itemstack, placer, pointed_thing, param2)
end

for i = 1,11 do
	minetest.register_node("beam_prototype:"..i, {
	    drawtype = "mesh",
		description = "beam piece "..i,
	    -- Holds the texture for each "material"
		tiles = {"default_frame.png"},

	    -- Path to the mesh
	    mesh = "beam_"..i..".obj",
	    groups = {cracky=3,oddly_breakable_by_hand=3,not_in_creative_inventory=0},
	    paramtype = 'light',paramtype2 = 'facedir',
	    on_place = function(itemstack, placer, pointed_thing)
			if pointed_thing.type ~= "node" then
				return itemstack
			end

			return rotate_and_place(itemstack, placer, pointed_thing)
		end,
	})
end
